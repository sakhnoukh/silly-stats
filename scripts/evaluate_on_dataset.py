"""
Evaluate Extractor on RVL-CDIP Gold Dataset (1980s scanned invoices)

Unlike evaluate_pipeline.py which runs on PDF files, this script works
directly on the raw_text already in data/processed/*.csv — no OCR needed.

Compares extractor versions against results/gold_dataset.csv which has
ground truth for three fields: recipient_name, invoice_number, total_amount.

Usage:
    python scripts/evaluate_on_dataset.py
    python scripts/evaluate_on_dataset.py --extractor scripts/extract_invoice_fields_v1.py
    python scripts/evaluate_on_dataset.py --extractor scripts/extract_invoice_fields_v0.py --output results/eval_dataset_v0.json
"""

import argparse
import importlib.util
import json
import re
import sys
from pathlib import Path

import pandas as pd

PROJ_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(PROJ_ROOT))

GOLD_CSV  = PROJ_ROOT / "results" / "gold_dataset.csv"
PROC_DIR  = PROJ_ROOT / "data" / "processed"

# Only 3 fields have gold labels in the dataset
EVAL_FIELDS = ["invoice_number", "recipient_name", "total_amount"]


# ---------------------------------------------------------------------------
# Dynamic extractor loading  (same as evaluate_pipeline.py)
# ---------------------------------------------------------------------------

def load_extractor(extractor_path: Path):
    spec   = importlib.util.spec_from_file_location("_extractor_module", extractor_path)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module.InvoiceExtractor


# ---------------------------------------------------------------------------
# Matching helpers  (same lenient rules as evaluate_pipeline.py)
# ---------------------------------------------------------------------------

def _clean(s) -> str:
    if not s:
        return ""
    return re.sub(r"\s+", " ", str(s)).strip().lower()


def _to_float(s):
    if not s:
        return None
    cleaned = re.sub(r"[^\d\.]", "", str(s))
    try:
        return float(cleaned) if cleaned else None
    except ValueError:
        return None


def match_string(gold, pred) -> bool:
    g, p = _clean(gold), _clean(pred)
    if not g or not p:
        return False
    return g in p or p in g


def match_invoice_number(gold, pred) -> bool:
    g = _clean(gold).lstrip("0")
    p = _clean(pred).lstrip("0")
    return bool(g) and g == p


def match_amount(gold, pred) -> bool:
    g, p = _to_float(gold), _to_float(pred)
    if g is None or p is None:
        return False
    return abs(g - p) <= max(0.01, 0.01 * abs(g))


MATCH_FN = {
    "invoice_number":  match_invoice_number,
    "recipient_name":  match_string,
    "total_amount":    match_amount,
}


# ---------------------------------------------------------------------------
# Load data
# ---------------------------------------------------------------------------

def load_texts() -> pd.DataFrame:
    """
    Load raw_text for all invoice-labelled documents from processed splits.
    Returns DataFrame with columns: doc_id, file_name, raw_text
    """
    dfs = []
    for split in ["train.csv", "val.csv", "test.csv"]:
        path = PROC_DIR / split
        if not path.exists():
            print(f"  [WARN] {split} not found — skipping")
            continue
        df = pd.read_csv(path)
        df = df[df["label"] == "invoice"].copy()
        dfs.append(df)

    if not dfs:
        print("ERROR: No processed CSVs found. Run the Phase 1 pipeline first.")
        sys.exit(1)

    combined = pd.concat(dfs, ignore_index=True)
    combined["raw_text"] = combined["raw_text"].fillna("")

    print(f"Loaded {len(combined)} invoice documents from processed splits")
    return combined


def load_gold() -> pd.DataFrame:
    if not GOLD_CSV.exists():
        print(f"ERROR: Gold dataset not found: {GOLD_CSV}")
        print("  This file is generated by scripts/build_gold_dataset.py")
        sys.exit(1)
    gold = pd.read_csv(GOLD_CSV)
    print(f"Loaded gold dataset: {len(gold)} rows")
    for field in EVAL_FIELDS:
        n = gold[field].notna().sum() if field in gold.columns else 0
        print(f"  {field:<25}: {n} docs with gold labels")
    return gold


# ---------------------------------------------------------------------------
# Evaluate
# ---------------------------------------------------------------------------

def evaluate(texts_df: pd.DataFrame, gold_df: pd.DataFrame,
             InvoiceExtractor) -> dict:
    """
    Run extractor on raw_text for each doc that has a gold label.
    Returns per-field accuracy results.
    """
    # Index texts by doc_id for fast lookup
    texts_idx = texts_df.set_index("doc_id") if "doc_id" in texts_df.columns else None

    field_correct = {f: 0 for f in EVAL_FIELDS}
    field_total   = {f: 0 for f in EVAL_FIELDS}
    per_doc       = {}

    extractor = InvoiceExtractor()

    # For each field that has gold labels, evaluate
    for field in EVAL_FIELDS:
        if field not in gold_df.columns:
            continue

        gold_field = gold_df[["doc_id", field]].dropna(subset=[field])

        for _, row in gold_field.iterrows():
            doc_id   = row["doc_id"]
            gold_val = row[field]

            # Get raw_text for this doc
            raw_text = ""
            if texts_idx is not None and doc_id in texts_idx.index:
                raw_text = str(texts_idx.loc[doc_id, "raw_text"])
            elif "file_name" in texts_df.columns:
                # Try matching by file_name if doc_id doesn't match directly
                fname_match = texts_df[texts_df["file_name"].str.contains(
                    str(doc_id).replace("doc_", ""), na=False
                )]
                if len(fname_match):
                    raw_text = str(fname_match.iloc[0]["raw_text"])

            if not raw_text.strip():
                continue

            # Run extraction
            try:
                extracted = extractor.extract(raw_text)
                pred_val  = extracted.get(field)
            except Exception:
                pred_val = None

            is_correct = MATCH_FN[field](gold_val, pred_val)
            field_correct[field] += int(is_correct)
            field_total[field]   += 1

            if doc_id not in per_doc:
                per_doc[doc_id] = {}
            per_doc[doc_id][field] = {
                "gold":      gold_val,
                "predicted": pred_val,
                "correct":   is_correct,
            }

    field_summary = {}
    for field in EVAL_FIELDS:
        total   = field_total[field]
        correct = field_correct[field]
        field_summary[field] = {
            "correct":  correct,
            "total":    total,
            "accuracy": round(correct / total, 3) if total else None,
        }

    return {"field_summary": field_summary, "per_doc": per_doc}


# ---------------------------------------------------------------------------
# Print report
# ---------------------------------------------------------------------------

def print_report(results: dict, extractor_label: str) -> None:
    summary = results["field_summary"]

    print()
    print(f"  Extractor: {extractor_label}")
    print(f"  Dataset:   RVL-CDIP gold (1980s scanned tobacco invoices)")
    print("=" * 60)
    print("  Per-field accuracy (3 fields with gold labels)")
    print("=" * 60)
    print(f"  {'Field':<25} {'Correct':>7}  {'Total':>5}  {'Accuracy':>9}")
    print("  " + "-" * 52)

    total_correct = 0
    total_fields  = 0
    for field, stats in summary.items():
        c   = stats["correct"]
        t   = stats["total"]
        acc = stats["accuracy"]
        bar = "█" * int((acc or 0) * 10)
        acc_str = f"{acc:.1%}" if acc is not None else "  N/A"
        print(f"  {field:<25} {c:>7}  {t:>5}  {acc_str:>9}  {bar}")
        total_correct += c
        total_fields  += t

    overall = total_correct / total_fields if total_fields else 0
    print("  " + "-" * 52)
    print(f"  {'OVERALL':<25} {total_correct:>7}  {total_fields:>5}  {overall:.1%}")
    print()


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description="Evaluate extractor on RVL-CDIP gold dataset",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python scripts/evaluate_on_dataset.py
  python scripts/evaluate_on_dataset.py --extractor scripts/extract_invoice_fields_v1.py
  python scripts/evaluate_on_dataset.py --extractor scripts/extract_invoice_fields_v0.py --output results/eval_dataset_v0.json
        """
    )
    parser.add_argument(
        "--extractor",
        default="scripts/extract_invoice_fields.py",
        help="Path to extractor .py file to test"
    )
    parser.add_argument(
        "--output", default=None,
        help="Save results JSON to this path"
    )
    args = parser.parse_args()

    extractor_path = PROJ_ROOT / args.extractor
    if not extractor_path.exists():
        print(f"ERROR: extractor not found: {extractor_path}")
        sys.exit(1)

    print(f"Loading extractor: {extractor_path.name}")
    InvoiceExtractor = load_extractor(extractor_path)
    print()

    texts_df = load_texts()
    gold_df  = load_gold()
    print()

    results = evaluate(texts_df, gold_df, InvoiceExtractor)
    print_report(results, extractor_path.name)

    if args.output:
        out_path = PROJ_ROOT / args.output
        out_path.parent.mkdir(parents=True, exist_ok=True)
        # Remove per_doc from saved output to keep file small
        save_results = {
            "extractor":     extractor_path.name,
            "dataset":       "RVL-CDIP gold",
            "field_summary": results["field_summary"],
        }
        with open(out_path, "w", encoding="utf-8") as f:
            json.dump(save_results, f, indent=2, ensure_ascii=False)
        print(f"Results saved to {out_path}")


if __name__ == "__main__":
    main()
